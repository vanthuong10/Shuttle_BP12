void selectMode(uint8_t sw)
{
	static bool run_one = false ;
	if(!run_one){
		manualTaskInit();
		autoTaskInit() ;
		dataUpdateTaskInit();
		safetyTaskInit();
		hydraulicTaskInit();
		run_one = true ;
		return ;
	}
	if(sw)
	{
		manualTaskSupend();
		autoTaskResume() ;
	}else
	{
		autoTaskSupend();
		manualTaskResume();
	}
	static uint8_t current_mode;
	if (current_mode != sw) {
		ResetOutput();
		SetSpeed(0, SetSpeedPDO[0]);
		current_mode = sw;
	}

}
